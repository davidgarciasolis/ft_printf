int	ft_putstr(char	*ptr)
{
	int	i;

	i = 0;
	if (!ptr)
		return (NULL);
	while (ptr[i])
	{
		if(ft_putchar(ptr[i]) == -1)
			return (-1);
		i++;
	}
	return (i);
}