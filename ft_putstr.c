#include "ft_printf.h"
#include <unistd.h>

int	ft_putstr(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i] != '\0')
	{
		n = n + ft_putchar(str[i]);
		i++;
	}
	return (n);
}